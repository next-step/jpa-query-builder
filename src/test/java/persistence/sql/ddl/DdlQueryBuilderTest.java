package persistence.sql.ddl;

import org.assertj.core.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

class DdlQueryBuilderTest {

    @Test
    @DisplayName("create table DDL query 테스트")
    void createTest() {
        DdlQueryBuilder ddlQueryBuilder = new DdlQueryBuilder(Person.class);
        String columnsDdl = ddlQueryBuilder.createTable();
        Assertions.assertThat(columnsDdl.trim().toLowerCase())
                .isEqualTo("create table users (old integer, nick_name varchar(255), id bigint generated by default as identity primary key, email varchar(255) not null )");
    }
}