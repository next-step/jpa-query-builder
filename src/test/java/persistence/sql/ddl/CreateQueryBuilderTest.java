package persistence.sql.ddl;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.params.ParameterizedTest;
import org.junit.jupiter.params.provider.Arguments;
import org.junit.jupiter.params.provider.MethodSource;
import persistence.entity.Person_Week1_Step2_Demand1;
import persistence.entity.Person_Week1_Step2_Demand2;
import persistence.entity.Person_Week1_Step2_Demand3;
import persistence.sql.Query;
import persistence.sql.dialect.h2.H2Query;

import java.util.stream.Stream;

import static org.assertj.core.api.Assertions.assertThat;
import static org.assertj.core.api.Assertions.assertThatThrownBy;

class CreateQueryBuilderTest {

    @DisplayName("@Entity 애노테이션이 붙은 클래스만 쿼리를 생성할 수 있다.")
    @Test
    void shouldFailWhenEntityIsNotAnnotated() {
        CreateQueryBuilder createQueryBuilder = new CreateQueryBuilder(new H2Query());
        assertThatThrownBy(() -> createQueryBuilder.getQuery(TestWithNoEntityAnnotation.class))
                .isInstanceOf(IllegalArgumentException.class);
    }

    private static class TestWithNoEntityAnnotation {
    }

    @DisplayName("엔티티에 알맞는 create 쿼리를 생성한다.")
    @ParameterizedTest
    @MethodSource("createQueryTestParam")
    void createQueryTest(Query query, Class<?> entityClass, String expectedQuery) {
        CreateQueryBuilder createQueryBuilder = new CreateQueryBuilder(query);
        String actualQuery = createQueryBuilder.getQuery(entityClass);
        assertThat(actualQuery).isEqualTo(expectedQuery);
    }

    private static Stream<Arguments> createQueryTestParam() {
        return Stream.of(
                Arguments.of(new H2Query(), Person_Week1_Step2_Demand1.class, "create table person_week1_step2_demand1 (id bigint not null, name varchar(255), age integer, primary key (id))"),
                Arguments.of(new H2Query(), Person_Week1_Step2_Demand2.class, "create table person_week1_step2_demand2 (id bigint generated by default as identity, nick_name varchar(255), old integer, email varchar(255) not null, primary key (id))"),
                Arguments.of(new H2Query(), Person_Week1_Step2_Demand3.class, "create table users (id bigint generated by default as identity, nick_name varchar(255), old integer, email varchar(255) not null, primary key (id))")
        );
    }

}
