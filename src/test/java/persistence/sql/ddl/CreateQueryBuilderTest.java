package persistence.sql.ddl;

import entity.Person_Week1_Step2_Demand1;
import entity.Person_Week1_Step2_Demand2;
import entity.Person_Week1_Step2_Demand3;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.params.ParameterizedTest;
import org.junit.jupiter.params.provider.Arguments;
import org.junit.jupiter.params.provider.MethodSource;
import persistence.sql.Dialect;
import persistence.sql.dialect.h2.H2Dialect;
import persistence.sql.entity.EntityData;

import java.util.stream.Stream;

import static org.assertj.core.api.Assertions.assertThat;
class CreateQueryBuilderTest {

    @DisplayName("엔티티에 알맞는 create 쿼리를 생성한다.")
    @ParameterizedTest
    @MethodSource("createQueryTestParam")
    void createQueryTest(Dialect dialect, Class<?> entityClass, String expectedQuery) {
        CreateQueryBuilder createQueryBuilder = new CreateQueryBuilder(dialect);
        String actualQuery = createQueryBuilder.generateQuery(new EntityData(entityClass));

        assertThat(actualQuery).isEqualTo(expectedQuery);
    }

    private static Stream<Arguments> createQueryTestParam() {
        return Stream.of(
                Arguments.of(new H2Dialect(), Person_Week1_Step2_Demand1.class, "create table person_week1_step2_demand1 (id bigint not null, name varchar(255), age integer, primary key (id))"),
                Arguments.of(new H2Dialect(), Person_Week1_Step2_Demand2.class, "create table person_week1_step2_demand2 (id bigint generated by default as identity, nick_name varchar(255), old integer, email varchar(255) not null, primary key (id))"),
                Arguments.of(new H2Dialect(), Person_Week1_Step2_Demand3.class, "create table users (id bigint generated by default as identity, nick_name varchar(255), old integer, email varchar(255) not null, primary key (id))")
        );
    }

}
